// destructuring

// easily extract array elements or object properties and store them in variables

//spread takes out all properties and puts them in new array or object
//destructuring allows take out single elements and store them


//array destructuring

  [a,b] = ["Hello", "Max"]
  console.log(a); // "Hello"
  console.log(b); // "Max"

//object destructuring
  //
  // {name} = {name: "Max", age: 28}
  // console.log(name); // Max
  // console.log(age); // undefined



/////////////////

const numbers = [1,2,3];
[num1, num2] = numbers;
